public class Primitive {
    public static void main(String[] args) {
        // Primitive Data Types
        int myAge = 20;
        char myGender = 'M';
        boolean isAdult = true;
        float myHeight = 170.5f;
        double myWeight = 60.5;
        String myName = "John Doe";
        byte myAgeInBytes = 20;
        short myAgeInShort = 20;
        long myAgeInLong = 209999999999L;
        char myGenderInChar = 'M';
        boolean myIsAdult = true;
        
        // Wrapper Classes
        Integer myAgeInInteger = 20;
        Character myGenderInCharacter = 'M';
        Boolean myIsAdultInBoolean = true;
        Float myHeightInFloat = 170.5f;
        Double myWeightInDouble = 60.5;
        String myNameInString = "John Doe";
        Byte myAgeInBytesInByte = 20;
        Short myAgeInShortInShort = 20;
        Long myAgeInLongInLong = 209999999999L;
        
    }
}
